/**
 * This functions adds one to its input
 * @param {number} input any number
 * @returns {number} that number, plus one
 */
function addOne (input) {
  return input + 1
}

/**
 * This function adds two to its input
 * @param {number} input any number
 * @returns {number} that number, plus two
 */
function addTwo (x) {
  return x + 2
}

/**
 * This function subtracts one from its input
 * @param {number} input any number
 * @retuens {number} that number, minus one
 */
function minusOne (x) {
  return x - 1
}
